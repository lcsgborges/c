/*
    Operadores Relacionais:

    menor que (a < b)
    maior que (a > b)
    menor ou igual (a <= b)
    maior ou igual (a >= b)
    igual (a == b)
    diferente (a != b)
*/

#include <stdio.h>

int main(){

    int a = 10, b = 20;

    printf("Resultado: %d\n", a > b); // falso = 0
    printf("Resultado: %d\n", a < b); // verdadeiro = 1

    return 0;
}