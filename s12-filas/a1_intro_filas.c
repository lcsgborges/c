/*
O que é uma fila:
    estrutura em que a prestação de um serviço é mais lento que a demanda:
    ex: filha de banco, fila de impressão, ...

    conjunto de operações:
    - inserir na fila: sempre insere no final da fila;
    - remover da fila: sempre tira o primeiro da fila;
    - imprimir fila

    - FIRST IN, FIRST OUT (FIFO)
    
    ponteiro do ultimo sempre aponta pra NULL;
*/